import type { Lang } from '../lib/types'

// Flat dot-keyed bilingual strings. Missing keys fall back to the key itself.
type Dict = Record<string, string>

/** Pure translation lookup with `{var}` interpolation. No React / store deps. */
export function translate(
  lang: Lang,
  key: string,
  vars?: Record<string, string | number>,
): string {
  const dict = STRINGS[lang] || STRINGS.zh
  let s = dict[key] ?? STRINGS.zh[key] ?? key
  if (vars) {
    for (const k of Object.keys(vars)) {
      s = s.split(`{${k}}`).join(String(vars[k]))
    }
  }
  return s
}

